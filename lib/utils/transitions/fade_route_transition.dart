import 'package:flutter/material.dart';

class FadeInRouteTransition extends PageRouteBuilder {
//  final Widget enterPage;
//  final Widget exitPage;

  final Widget page;

  FadeInRouteTransition({required this.page})
      : super(
          pageBuilder: (
            BuildContext context,
            Animation<double> animation,
            Animation<double> secondaryAnimation,
          ) =>
              page,
          transitionDuration: const Duration(milliseconds: 232),
          transitionsBuilder: (
            BuildContext context,
            Animation<double> animation,
            Animation<double> secondaryAnimation,
            Widget child,
          ) =>
              FadeTransition(
            opacity: animation,
            child: child,
          ),
        );
}
